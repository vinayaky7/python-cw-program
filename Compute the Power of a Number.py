########################Using ** Operator:################################################
def power(base, exponent):
    result = base ** exponent
    return result

# Example usage:
if __name__ == "__main__":
    base = 2
    exponent = 3
    result = power(base, exponent)
    print(f"{base} raised to the power of {exponent} is: {result}")

